"""
全局配置模块

所有可配置项集中管理于此。其他模块通过 `from data_infra.config import settings` 引用配置，
不允许在业务代码中硬编码交易对、路径、API参数等。

配置加载优先级:
    1. .env 文件中的环境变量（敏感信息如 API Key）
    2. 本文件中的默认值（非敏感配置）

依赖: 无（最底层模块）
被依赖: 所有其他模块
"""

import os
from pathlib import Path

from dotenv import load_dotenv

# ============================================================================
# 加载 .env 文件
# ============================================================================
# 项目根目录: 从 data_infra/config/settings.py 向上三级
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent

# 加载 .env（如果存在）
load_dotenv(PROJECT_ROOT / ".env")

# ============================================================================
# 交易所配置
# ============================================================================
# 交易所标识，ccxt 使用此值创建交易所实例
# 预留切换能力: 改为 "okx" 即可接入 OKX（需同步更改 API Key）
EXCHANGE_ID = "binance"

# API 密钥，从 .env 文件读取，不在代码中硬编码
# .env 文件格式:
#   BINANCE_API_KEY=your_api_key_here
#   BINANCE_SECRET=your_secret_here
API_KEY = os.getenv("BINANCE_API_KEY", "")
API_SECRET = os.getenv("BINANCE_SECRET", "")

# 代理配置（可选，用于本地开发环境访问交易所）
# 云服务器部署时通常不需要代理
PROXY_HOST = os.getenv("PROXY_HOST", "")
PROXY_PORT = os.getenv("PROXY_PORT", "")

# ============================================================================
# 交易对配置
# ============================================================================
# 当前关注的交易对列表
# 所有下游模块从此处读取，新增币对只需修改这个列表
SYMBOLS = [
    "BTC/USDT",
    "DOGE/USDT",
    "SOL/USDT",
    "BNB/USDT",
    "ETH/USDT",
]

# ============================================================================
# K线采集配置
# ============================================================================
# 只采集 1m K线，其他周期（5m, 15m, 1h, 4h, 1d）由 DataReader 通过
# aggregator.resample_ohlcv() 从 1m 按需降采样生成。
# 这样避免了:
#   1. 浪费 API 配额（减少 5 倍请求量）
#   2. 不同周期数据不一致的风险
KLINE_COLLECT_TIMEFRAME = "1m"

# Binance API 每次请求最多返回 1000 根 K线
KLINE_LIMIT = 1000

# K线采集轮询间隔（秒）
# 1m K线每 60 秒产生一根，所以 60 秒轮询一次刚好
KLINE_COLLECT_INTERVAL = 60

# ============================================================================
# 逐笔成交采集配置
# ============================================================================
# Binance API 每次请求最多返回 1000 条逐笔成交
TICK_FETCH_LIMIT = 1000

# 追上实时数据后的等待间隔（秒）
# 追赶模式下满额(1000条)时立即继续，不等待
# 只有追上实时（返回不满额）后才等待此间隔
TICK_IDLE_INTERVAL = 2

# ============================================================================
# Tick 冷启动策略
# ============================================================================
# 新币对首次采集时，如何确定起始位置:
#   "latest"    — 从当前时刻开始采集（放弃历史数据，快速启动）
#   "from_date" — 从 TICK_COLD_START_DATE 指定的日期开始（需要长时间追赶）
TICK_COLD_START_MODE = "latest"

# 仅在 TICK_COLD_START_MODE = "from_date" 时生效
# 格式: "YYYY-MM-DD"
TICK_COLD_START_DATE = "2024-01-01"

# ============================================================================
# 订单簿采集配置
# ============================================================================
# 订单簿档位深度: 5 / 10 / 20
# 10档可满足大多数 LOB (Limit Order Book) 因子的需求
ORDERBOOK_DEPTH = 10

# WebSocket 推送频率: "100ms" 或 "1000ms"
# 100ms 是 Binance 提供的最高粒度
ORDERBOOK_UPDATE_SPEED = "100ms"

# ============================================================================
# 合约市场数据采集配置
# ============================================================================
# 采集轮询间隔（秒）
# 持仓量、多空比、主动买卖量等数据更新频率约为 5 分钟
MARKET_COLLECT_INTERVAL = 300

# 资金费率更新间隔（秒），Binance 永续合约每 8 小时结算一次
# 实际采集中每轮都会查询以防遗漏，此值仅用于参考
FUNDING_RATE_INTERVAL = 28800

# ============================================================================
# 存储路径配置
# ============================================================================
# 所有路径相对于项目根目录
DB_DIR = str(PROJECT_ROOT / "db")
KLINE_DB_PATH = str(PROJECT_ROOT / "db" / "kline.db")
MARKET_DB_PATH = str(PROJECT_ROOT / "db" / "market.db")
TICK_DATA_DIR = str(PROJECT_ROOT / "db" / "ticks")
ORDERBOOK_DATA_DIR = str(PROJECT_ROOT / "db" / "orderbook")
LOG_DIR = str(PROJECT_ROOT / "logs")

# ============================================================================
# 容错配置
# ============================================================================
# 单次 API 请求最大重试次数
# 重试采用指数退避策略: 等待 RETRY_BASE_DELAY × 2^n 秒
# 例: 5次重试的等待时间依次为 1, 2, 4, 8, 16 秒
MAX_RETRIES = 5

# 重试初始等待时间（秒）
RETRY_BASE_DELAY = 1.0

# 遇到 API 限频 (HTTP 429) 后的等待时间（秒）
# Binance 限频惩罚较重，需要等待较长时间
RATE_LIMIT_PAUSE = 30

# 单次 API 请求超时时间（秒）
REQUEST_TIMEOUT = 30

# WebSocket 断线重连间隔（秒）
# 采用指数退避: WS_RECONNECT_DELAY × 2^n，上限 60 秒
WS_RECONNECT_DELAY = 5

# WebSocket 心跳间隔（秒）
# 定期发送 ping 帧以保持连接活跃
WS_PING_INTERVAL = 30

# ============================================================================
# 运行状态监控配置
# ============================================================================
# 心跳日志输出间隔（秒）
# 每隔此时间输出一条采集摘要日志，用于确认脚本在正常运行
HEARTBEAT_INTERVAL = 300  # 5 分钟

# 状态文件更新间隔（秒）
# 每隔此时间将运行状态写入 .status.json 文件
# 外部可通过 scripts/status.py 读取这些文件查看各脚本状态
STATUS_FILE_UPDATE_INTERVAL = 60  # 1 分钟
